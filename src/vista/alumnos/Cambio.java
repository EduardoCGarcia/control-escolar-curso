    private void btnCambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambioActionPerformed
        Alumno nuevo;
        try {
            nuevo = new Alumno(
                    txtNombre.getText(),
                    (int) spnEdad.getValue(),
                    txtNumeroDeCuenta.getText(),
                    txtCorreo.getText(),
                    new Direccion(
                            txtCalle.getText(),
                            txtCodigoPostal.getText(),
                            Integer.parseInt(txtNumero.getText()))
            );
            CambiarController.cambiarDato(app.indice, nuevo);
            CambiarController.preCambioBorrado(txtNombre, spnEdad, txtNumeroDeCuenta, txtCorreo, txtCalle, txtCodigoPostal, txtNumero);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_btnCambioActionPerformed


